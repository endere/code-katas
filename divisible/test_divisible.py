"""Test for the divisible by 6 code code."""


import pytest

PARAMS_TABLE = [
        ("1*0", ["120", "150", "180"]),
        ("*", ["6"]),
        ("*1", []),
        ("*2", ["12", "42", "72"]),
        ("81234567890*", ["812345678904"]),
        ("41*", ["414"]),
        ("*6", ["6", "36", "66", "96"]),
        ("2345*345729", []),
        ("34234*2", ["3423402", "3423432", "3423462", "3423492"]),
        ("1234567890123456789012345678*0", ["123456789012345678901234567800", "123456789012345678901234567830", "123456789012345678901234567860", "123456789012345678901234567890"]),
        ("4*", ["42", "48"]),
        ("2*", ["24"]),
        ("2*00", ["2100", "2400", "2700"]),
        ("5555*", ["55554"])

]


@pytest.mark.parametrize("n, result", PARAMS_TABLE)
def test_divis(n, result):
    """Test if divisible by 6 function outputs correct answer."""
    from divisible import is_divisible_by_6
    assert is_divisible_by_6(n) == result
